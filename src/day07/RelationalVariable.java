package day07;

public class RelationalVariable {
    public static void main(String[] args) {

        // > >= < <= == != Relational Operator
        //is used to compare 2 numbers
        //and return true or false
        //anything action or expression
        ////that return true or false result
        //can be called boolean expression
int myNumber = 10;
        System.out.println(myNumber>5);
        System.out.println(myNumber< -19 );
        System.out.println(myNumber >= 5);
        System.out.println(myNumber>=8);
        System.out.println(myNumber<=5);
        System.out.println(myNumber>=8);

        //== is used to check for equality
        //and very differnt from single =


        //s difference betwen == and = same as differnce between
        // equal and equality in daily English
        System.out.println(myNumber==5);

        // != is used to check for inequality,
        // ! in programming translate to NOT
        System.out.println(myNumber != 10);
        System.out.println(myNumber!= 6 );

        // == and != are questions, checking for equality
        // or inequality
    }
}



